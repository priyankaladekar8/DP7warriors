package com.stars;

public class LeftStar{ 

		
				/*
				  * * * * *
				  * * * *
				  * * *
				  * * 
				  * 
				 */
				public static void main(String[]args) {
					for (int i=5; i>=1; i--)
					{
						for(int j=1; j<=5; j++)
						{
							System.out.println("* ");
						}
						System.out.println();
					}
				}

			}






